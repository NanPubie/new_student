package cza2.looploop;

public class Test5 {
    /*
    * 制表符\t
    * 长度可变的大空格，打印表格时候，可以用来上下对齐
    *
    *
    * */
   static void main(String[] args) {
        System.out.println("张三\t男\t\t18");
        System.out.println("李四\t女\t\t19");
        System.out.println("王五\t男\t\t20");
//    如果输入一个\t还没有对齐，可能是因为上下长度差异过大，就要输入两个\t来解决
       System.out.println("------------------------------");
       System.out.println("-\t\t-");
       System.out.println("-\t-");
       System.out.println("-\t\t\t-");


    }


}
