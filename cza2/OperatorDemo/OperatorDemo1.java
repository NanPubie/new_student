package cza2.OperatorDemo;

public class OperatorDemo1 {
    public static void main(String[] args) {
//    算数运算符的运用
//        整数运算
        int a = 10;
        int b = 5;
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);
//        小数运算
        double c = 10.0;
        double d = 3.0;
        System.out.println(c + d);
        System.out.println(c - d);
        System.out.println(c * d);
        System.out.println(c / d);
        System.out.println(c % d);


    }
}
